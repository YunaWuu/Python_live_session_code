# sequence of 10 integers starting at zero
for i in range(10):
    print(i)
print('\n########################\n')
# start 1, stop 11 exclusive
for i in range(1, 11):
    print(i)
print('\n########################\n')
# start 2, stop 11 exclusive, step 2
for i in range(2, 11, 2):
    print(i)
print('\n########################\n')
# start 10, stop 11 exclusive, step 2
for i in range(10, 101, 10):
    print(i)
print('\n########################\n')
# start 100, stop 9 exclusive, step -10
for i in range(100, 9, -10):
    print(i)